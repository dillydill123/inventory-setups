	static
	{
		idsToFilter = new HashSet<>();
		safeIdsOverride = new HashSet<>();

		// Castle wars capes have the same name as the treasure trail items, but are fundamentally different items.
		// The castle wars capes will be untradeable while the treasure trail will be tradeable.
		safeIdsOverride.add(ItemID.CASTLEWARS_CLOAK_SARADOMIN);
		safeIdsOverride.add(ItemID.TRAIL_SARADOMIN_CLOAK);
		safeIdsOverride.add(ItemID.CASTLEWARS_CLOAK_ZAMORAK);
		safeIdsOverride.add(ItemID.TRAIL_ZAMORAK_CLOAK);
	}

	public boolean shouldKeep(int itemID)
	{
		return !idsToFilter.contains(itemID);
	}

	public void estimateFakeItems(ItemManager itemManager)
	{
		addLMSItemsToFilter();
		addNightmarezoneItemsToFilter();
		addClueScrollItemsToFilter();
		addMiscellaneousItemsToFilter();

		// Estimate fake items by collecting items with duplicate inventory models. If a tradeable item exists among the
		// nontradeable items, we add the nontradeable items to the list of IDs to filter. If all items are tradeable
		// or untradeable, do nothing. Note that in F2P, even tradeable members items are considered untradeable, so
		// this heuristic will not work there.
		Map<String, List<ItemComposition>> nameToItemComps = new HashMap<>();

		for (int id = 0; id < highestItemID; id++)
		{
			ItemComposition itemComp = itemManager.getItemComposition(id);
			// Skip noted and placeholder items.
			if (itemComp.getNote() != -1 || itemComp.getPlaceholderTemplateId() != -1)
			{
				continue;
			}

			String membersName = itemComp.getMembersName();
			// Filter any object that has "null" as the name
			if (membersName == null || membersName.isEmpty() || membersName.equalsIgnoreCase("null"))
			{
				idsToFilter.add(id);
				continue;
			}

			nameToItemComps.computeIfAbsent(membersName, k -> new ArrayList<>()).add(itemComp);
		}

		for (Map.Entry<String, List<ItemComposition>> entry : nameToItemComps.entrySet())
		{
			List<ItemComposition> itemComps = entry.getValue();

			if (itemComps.size() < 2)
			{
				continue;
			}

			List<ItemComposition> tradeable = new ArrayList<>();
			List<ItemComposition> untradeable = new ArrayList<>();

			for (ItemComposition itemComp : itemComps)
			{
				if (itemComp.isTradeable())
				{
					tradeable.add(itemComp);
				}
				else
				{
					untradeable.add(itemComp);
				}
			}

			// If one item is tradeable and the others are not, remove the untradeable items
			// It is possible that two items with the same name can be completely different. For example, the castle
			// wars cloaks are named exactly as the treasure trail cloaks (Saradomin/Zamorak cloak). For these special
			// cases, we add them to the safeIdsOverride.
			if (!tradeable.isEmpty() && !untradeable.isEmpty())
			{
				for (ItemComposition itemComp : untradeable)
				{
					idsToFilter.add(itemComp.getId());
				}
			}
		}

		// Remove any items that accidentally would get filtered that we know for sure should not.
		idsToFilter.removeAll(safeIdsOverride);
	}

